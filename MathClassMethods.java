
package mathclassmethods;


public class MathClassMethods {

    
    public static void main(String[] args) {
        System.out.println(Math.abs(-26.5));
        System.out.println(Math.ceil(8.3 ));
        System.out.println(Math.floor(8.9 ));
        System.out.println(Math.max(3.4,6.9));
        System.out.println(Math.min(8.3,2.9 ));
        System.out.println(Math.pow(10,4));
        System.out.println(Math.sqrt(1024 ));
    }
    
}
